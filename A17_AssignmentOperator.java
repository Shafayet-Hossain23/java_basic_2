public class A17_AssignmentOperator {
    public static void main(String[] args) {
        int x=50;
        int y=40;
        
        x+=y;  //x=x+5
        System.out.println(""+x);
        
        x-=y; 
        System.out.println(""+x);
        
        x*=y; 
        System.out.println(""+x);
        
        x/=y; 
        System.out.println(""+x);
        
        x%=y; 
        System.out.println(""+x);
    }
}
